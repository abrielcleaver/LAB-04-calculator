export function add_two(num){
    return num + 2;
}

export function add(num1, num2) {
    return num1 + num2;
}

export function subtraction(num1, num2) {
    return num1 - num2;
}

export function multiplication(num1, num2) {
    return num1 * num2;
}

export function division(num1, num2) {
    return num1 / num2;
}